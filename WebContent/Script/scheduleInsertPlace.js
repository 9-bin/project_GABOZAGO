function addPlace1(){
	var placenum = document.getElementById('placenum1').value;
	var userid = document.getElementById('userid').value;
	var schedulename = document.getElementById('schedulename').value;
	if(confirm("일정에 추가하시겠습니까?")){
		location.href="scheduleAdding.do?userid="+userid+"&placenum="+placenum+"&schedulename="+schedulename;
		alert("일정에 추가되었습니다.")
	}else{
		history.go(0);
	}
}
