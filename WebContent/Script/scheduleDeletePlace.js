function deletePlace1(){
	var schedulename = document.getElementById('schedulename').innerText;
	var userid = document.getElementById('userid').value;
	var placenum = document.getElementById('Placenum1').value;
	if(confirm("삭제하시겠습니까?")){
		location.href="scheduledeletePlace.do?userid="+userid+"&placenum="+placenum+"&schedulename="+schedulename;
		alert("삭제되었습니다.")
	}else{
		history.go(0);
	}
}
